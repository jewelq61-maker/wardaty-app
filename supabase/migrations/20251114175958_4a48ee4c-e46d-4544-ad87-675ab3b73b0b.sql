-- Fix database functions to prevent search path manipulation attacks
-- Add SET search_path = public to all trigger functions

CREATE OR REPLACE FUNCTION public.update_pregnancy_updated_at()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $function$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$function$;

